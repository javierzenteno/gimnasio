package Modelos;

public class Etapa {
	private Long codeta,codtor;
	private short etapa;
	private boolean esteta;
	public Long getCodeta() {
		return codeta;
	}
	public void setCodeta(Long codeta) {
		this.codeta = codeta;
	}
	public Long getCodtor() {
		return codtor;
	}
	public void setCodtor(Long codtor) {
		this.codtor = codtor;
	}
	public short getEtapa() {
		return etapa;
	}
	public void setEtapa(short etapa) {
		this.etapa = etapa;
	}
	public boolean isEsteta() {
		return esteta;
	}
	public void setEsteta(boolean esteta) {
		this.esteta = esteta;
	}
}
