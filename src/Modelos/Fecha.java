package Modelos;

public class Fecha {
	private int codfec;
	private int codlig;
	private float insfec;
	private String fecha;
	private boolean estfec;
	public int getCodfec() {
		return codfec;
	}
	public void setCodfec(int codfec) {
		this.codfec = codfec;
	}
	public int getCodlig() {
		return codlig;
	}
	public void setCodlig(int codlig) {
		this.codlig = codlig;
	}
	public float getInsfec() {
		return insfec;
	}
	public void setInsfec(float insfec) {
		this.insfec = insfec;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public boolean isEstfec() {
		return estfec;
	}
	public void setEstfec(boolean estfec) {
		this.estfec = estfec;
	}
	
}
